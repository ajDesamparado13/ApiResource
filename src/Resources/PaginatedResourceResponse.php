<?php

namespace Freedom\ApiResource\Resources;

use Illuminate\Http\Resources\Json\PaginatedResourceResponse as BaseResourceResponse;
use Illuminate\Support\Arr;

class PaginatedResourceResponse extends BaseResourceResponse
{
    /**
     * Add the pagination information to the response.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    protected function paginationInformation($request)
    {
        $paginated = $this->resource->resource->toArray();

        return [
            'meta' => $this->meta( $paginated),
        ];
    }

    /**
     * Gather the meta data for the response.
     *
     * @param  array  $paginated
     * @return array
     */
    protected function meta($paginated)
    {
        return ['pagination' => Arr::except($paginated, [
            'data',
            'first_page_url',
            'last_page_url',
            'prev_page_url',
            'next_page_url',
        ])];
    }


}
